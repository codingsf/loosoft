package cn.loosoft.stuwork.arch.entity.returnlog;

import java.util.Date;

import javax.persistence.Entity;
import javax.persistence.Table;

import org.hibernate.annotations.Cache;
import org.hibernate.annotations.CacheConcurrencyStrategy;

import cn.loosoft.stuwork.arch.entity.BaseLog;

/**
 * 
 * returnlog(档案归档) entity class
 * 
 * @author shanru.wu
 * @version 1.0
 * @since 2010-12-18
 */
@Entity
@Table(name = "returnlog")
@Cache(usage = CacheConcurrencyStrategy.READ_WRITE)
public class ReturnLog extends BaseLog
{

    private Long lendNum; // 调阅编号

    public Long getLendNum()
    {
        return lendNum;
    }

    public void setLendNum(Long lendNum)
    {
        this.lendNum = lendNum;
    }

    private Date returnDate; // 归档时间

    public Date getReturnDate()
    {
        return returnDate;
    }

    public void setReturnDate(Date returnDate)
    {
        this.returnDate = returnDate;
    }
}
