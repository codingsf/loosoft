package cn.loosoft.stuwork.arch.entity.sys;

import javax.persistence.Entity;
import javax.persistence.Table;

import org.hibernate.annotations.Cache;
import org.hibernate.annotations.CacheConcurrencyStrategy;

import cn.loosoft.stuwork.common.entity.IdEntity;

/**
 * 
 * dictionary(系统字典) entity class
 * 
 * @author shanru.wu
 * @version 1.0
 * @since 2010-12-17
 */
@Entity
@Table(name = "dictionary")
@Cache(usage = CacheConcurrencyStrategy.READ_WRITE)
public class Dictionary extends IdEntity
{

    public static String RKCL = "rkcl"; // 入库材料

    public static String DDLY = "ddly"; // 调档理由

    public static String DDFS = "ddfs"; // 调档方式

    public static String RKLY = "rkly"; // 入库理由

    private String       type;         // 类型

    private String       value;        // 显示值

    public String getType()
    {
        return type;
    }

    public void setType(String type)
    {
        this.type = type;
    }

    public String getValue()
    {
        return value;
    }

    public void setValue(String value)
    {
        this.value = value;
    }
}
